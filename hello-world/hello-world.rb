# Putting value on variables
name = 'Thiago'
color = 'Teal'
number = 10

# Concating variables
puts 'Hello  ' + name + '. Welcome to the Ruby world!'
puts name + 'test' # Thiagotest
puts "#{name} test" # Thiago teste

# Variable interpolation
puts "Hello #{name}. Welcome to the Ruby world!"